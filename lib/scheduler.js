/**
 * 生成每日亲亲提醒
 */
function generateKissSchedules(startDate, endDate) {
  const schedules = [];
  let d = new Date(startDate + 'T00:00:00+08:00');
  const end = new Date(endDate + 'T00:00:00+08:00');

  while (d <= end) {
    const dateStr = formatDate(d);
    schedules.push({
      id: `kiss-${dateStr}`,
      title: '睡前亲亲 💋',
      type: 'kiss',
      person: '郦赟 → 陈思婧',
      date: dateStr,
      time: 'bedtime',
      description: '郦赟睡前亲亲陈思婧，每日必修课 ❤️',
      repeatRule: 'daily',
      category: 'intimacy',
      autoGenerated: true,
    });
    d.setDate(d.getDate() + 1);
  }

  return schedules;
}

/**
 * 主入口：生成所有日程
 * @param {Object} storedSchedules - 数据库中已存储的日程数据 { completed: [], custom: [] }
 * @returns {Array} 合并后的日程列表
 */
function generateAllSchedules(storedSchedules) {
  const today = formatDate(new Date());
  const endDate = addDays(today, SCHEDULE_RANGE_DAYS);

  // 1. 生成航班
  const flights = generateFlightSchedules(today, endDate);

  // 2. 生成购票提醒
  const tickets = generateTicketReminders(flights);

  // 3. 生成猫咪喂食
  const feedings = generateFeedingSchedules(today, endDate);

  // 4. 生成水粮检查
  const waterChecks = generateWaterCheckSchedules(today, endDate);

  // 5. 生成每日亲亲
  const kisses = generateKissSchedules(today, endDate);

  // 合并所有自动生成日程
  let allGenerated = [...flights, ...tickets, ...feedings, ...waterChecks, ...kisses];

  // 去重（按 id）
  const seen = new Set();
  allGenerated = allGenerated.filter(s => {
    if (seen.has(s.id)) return false;
    seen.add(s.id);
    return true;
  });

  // 按日期排序
  allGenerated.sort((a, b) => a.date.localeCompare(b.date));

  // 为所有日程添加 completed 字段
  const stored = storedSchedules || { completed: [], custom: [] };
  const completedIds = new Set(stored.completed.map(c => c.id));
  const customSchedules = stored.custom || [];

  // 合并完成状态
  allGenerated = allGenerated.map(s => ({
    ...s,
    completed: completedIds.has(s.id),
  }));

  // 保留之前已完成但现在已过期（不在生成范围内）的日程
  const generatedIds = new Set(allGenerated.map(s => s.id));
  const oldCompleted = stored.completed.filter(c => !generatedIds.has(c.id));

  // 合并所有日程：生成的 + 自定义 + 旧的已完成
  const allSchedules = [...allGenerated, ...customSchedules, ...oldCompleted];

  return allSchedules;
}

module.exports = {
  generateAllSchedules,
  generateFlightSchedules,
  generateFeedingSchedules,
  generateWaterCheckSchedules,
  generateKissSchedules,
  generateTicketReminders,
};
